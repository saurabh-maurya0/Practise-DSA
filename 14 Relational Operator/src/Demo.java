public class Demo {
    public static void main(String args[])
    {
        int A = 10;
        int B = 5;
        System.out.println(A==B);   //F
        System.out.println(A!=B);   //T
        System.out.println(A>B);   //T
        System.out.println(A<B);   //F
        System.out.println(A>=B);   //T
        System.out.println(A<=B);   //F
    }
}
